  function(self, event, ...)
   local count, customcount = MuteAllSounds()
   if customcount > 0 then
      MAWS_Print(count+customcount.." sounds have been muted. ("..customcount.." custom sounds)")
   else 
      MAWS_Print(count.." Sounds have been muted.")
   end
end)

