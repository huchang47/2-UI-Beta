local PlayerScoreData = {}


function GetPlayerScoreData()
	local maxscore = 0
	for k,v in pairs(SkadaPerCharDB.total.players) do
		PlayerScoreData[v.name] = round((v.damage+v.healing-v.damagetaken) / 10000, 1)+v.interrupts+v.dispells-#v.deaths or 0;
		if PlayerScoreData[v.name]>maxscore then maxscore=PlayerScoreData[v.name] end
	end
	return PlayerScoreData,maxscore
end
